
var nome = prompt("Digite seu nome")// caixa entrada


var n = parseInt(prompt("Olá" + " " + nome + ", digite um numero"))// caixa entrada


document.write ("Seja bem vindo " + "  "+ nome + "<br>")
document.write ("Você digitou o numero_" + n + "<br>")

document.write ("O retorno da comparação boolena é_" + (n==n) + "<br>")
document.write ("A soma dos valores é_" + (n+n) + "<br>")
document.write ("A subtração dos valores é_" + (n-n) + "<br>")
document.write ("O resto da divisão é_" + (n%n) + "<br>")
document.write ("o quadrado do numero digitado é_ " + (n*n) + "<br>")

